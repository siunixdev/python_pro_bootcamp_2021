# Python Primitive Data Types
# Basic Data type String, Integer, FLoat Boolean

# String
print("Hello"[0])
print("Hello"[4])

keywords = "Hello"
print(keywords[len(keywords) - 1])

print("Hi Dunia"[2]) # will be print the space between Hi and Dunia

# This is not number
print("234" + "345")

# Integer -> number data types
print(123 + 345) # it will be count
print(123_456) # in python underscore in number will be ignore

number = 123456
print(type(number))

# Float -> for decimal number
phi = 3.14159
print(phi)
print(type(phi))

# Boolean -> value is True of False
print(True)
print(False)
